// O que são propriedades?
// Propriedades são informações que podemos retirar de algum valor
// Todos os valores em js tem propriedades(exceto null e undefined)

// Podemos acessar as propriedades de duas formas:
const arrNumbers = [1, 4, 6, 8]
console.log("Forma mais comum: " + arrNumbers.length) // forma mais usada
console.log(arrNumbers["length"])
